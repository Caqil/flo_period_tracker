  static String getCategoryIcon(String category) {
    switch (category.toLowerCase()) {
      case 'physical':
        return physicalSymptoms;
      case 'emotional':
        return emotionalSymptoms;
      case 'digestive':
        return digestiveSymptoms;
      case 'sleep':
        return sleepSymptoms;
      case 'skin':
